	var user string
	cmd := &cobra.Command{
		Use:   "delete <application-credential>",
		Short: "Delete an application credential",
		Args:  cobra.ExactArgs(1),
		RunE: func(cmd *cobra.Command, args []string) error {
			if err := o.Validate(); err != nil {
				return err
			}
			ctx := cmd.Context()
			client, ac, err := newIdentityAuthClient(ctx, a)
			if err != nil {
				return err
			}
			userID, err := resolveAppCredUser(ctx, client, ac, user)
			if err != nil {
				return err
			}
			return runAppCredDelete(ctx, client, userID, args[0])
		},
	}
	cmd.Flags().StringVar(&user, "user", "", "owning user (name or ID; defaults to the current user)")
